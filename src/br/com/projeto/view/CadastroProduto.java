    public void atualizaFornecedor () throws SQLException{
        
        Connection conexao = new Conexao().getConnection(); 
        FornecedorDAO fornecedorDao = new FornecedorDAO(conexao);
        ArrayList<Fornecedores> listaFornecedores = fornecedorDao.selectAll();
        DefaultComboBoxModel comboBoxModel1 = (DefaultComboBoxModel) cbFornecedor.getModel();
        
        for (Fornecedores fornecedor : listaFornecedores){
            comboBoxModel1.addElement(fornecedor);
        }
        
    }
    
    public void insertProduto () {
        
        Produto produto = new Produto();
        produto.setNomeProduto(txtNomeProduto.getText().trim());
        produto.setPreco(Double.parseDouble(txtPreco.getText().trim().replaceAll(",", ".")));
        produto.setQtd_estoque(Integer.parseInt(txtQuantidade.getText().trim()));
        
        Fornecedores fornecedor = (Fornecedores) cbFornecedor.getSelectedItem();
        produto.setFornecedor(fornecedor);
        
        Connection conexao;
        try {
            conexao = new Conexao().getConnection();
            ProdutoDAO produtoDao = new ProdutoDAO(conexao);
        
            produtoDao.insertProduto(produto);
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso !!!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar !!!" + ex);
        }
        
    }
    
